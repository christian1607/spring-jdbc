package pvt.auna.solben.model.bean;

public class SolicitudInscripBean {
	private String nombreMac;
	private String medicoTratante;
	private String nroScgSolben;
	private String medicoAuditor;
	private String nombDocument;
	private String rutaDocument;
	
	public SolicitudInscripBean() {}

	public String getNombreMac() {
		return nombreMac;
	}

	public void setNombreMac(String nombreMac) {
		this.nombreMac = nombreMac;
	}

	public String getMedicoTratante() {
		return medicoTratante;
	}

	public void setMedicoTratante(String medicoTratante) {
		this.medicoTratante = medicoTratante;
	}

	public String getNroScgSolben() {
		return nroScgSolben;
	}

	public void setNroScgSolben(String nroScgSolben) {
		this.nroScgSolben = nroScgSolben;
	}

	public String getMedicoAuditor() {
		return medicoAuditor;
	}

	public void setMedicoAuditor(String medicoAuditor) {
		this.medicoAuditor = medicoAuditor;
	}

	public String getRutaDocument() {
		return rutaDocument;
	}

	public void setRutaDocument(String rutaDocument) {
		this.rutaDocument = rutaDocument;
	}

	public String getNombDocument() {
		return nombDocument;
	}

	public void setNombDocument(String nombDocument) {
		this.nombDocument = nombDocument;
	}
	
	
}
